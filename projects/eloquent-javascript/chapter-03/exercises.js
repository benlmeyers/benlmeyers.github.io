//write a function that finds the smaller number out of 2 possibilities
function min(x, y) {
    return Math.min(x, y);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//write a functio that tests if a given number is even
function isEven(x) {
    if (x % 2 === 0) {
        return true;
    } else {
        return false;
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//write a function that takes two parameters, and counts how many of the second are in the first
function countChars(string, x) {
 var count = 0;
   for (var i = 0; i <= string.length; i++) {
        if (string[i] === x) {
           count++;
        } 
   } return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//write a function that counts 'B's in a given string
function countBs(string) {
   var count = 0;
   for (var i = 0; i <= string.length; i++) {
        if (string[i] === "B") {
           count++;
        } 
   } return count;
}

